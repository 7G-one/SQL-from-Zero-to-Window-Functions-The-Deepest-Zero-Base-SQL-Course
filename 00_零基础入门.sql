-- 第一节：什么是数据库？
-- =============================================

/*
 * 【什么是数据库？】
 *
 * 数据库（Database）就是"存放数据的仓库"。
 *
 * 生活类比：
 *   想象一个巨大的图书馆——
 *   - 图书馆 = 数据库（存放所有书籍的地方）
 *   - 书架 = 数据表（按类别存放书籍）
 *   - 一本书 = 一行数据（一条具体的信息）
 *   - 书的属性（书名、作者、ISBN）= 列（字段）
 *
 * 为什么需要数据库？
 *   1. 你用 Excel 记录过东西吧？Excel 适合个人少量数据
 *   2. 但如果有 100 万人的数据呢？Excel 会卡死！
 *   3. 数据库就是为了解决这个问题——高效存储和管理海量数据
 *
 * 数据库能做什么？
 *   - 存储用户信息（微信的用户数据）
 *   - 存储商品信息（淘宝的商品数据）
 *   - 存储交易记录（支付宝的转账记录）
 *   - 存储游戏数据（你的游戏存档）
 */

-- =============================================
-- 第二节：数据库的分类
-- =============================================

/*
 * 【关系型数据库（RDBMS）—— 重点学习】
 *
 * 数据以"表格"形式存储，表与表之间可以有关联关系
 * 就像图书馆里，"借阅记录"表会关联"读者"表和"书籍"表
 *
 * 常见的关系型数据库：
 *   1. MySQL     —— 最流行的开源数据库，互联网公司最爱
 *   2. PostgreSQL —— 功能最强大的开源数据库
 *   3. SQLite    —— 最轻量的数据库，手机App常用
 *   4. Oracle    —— 企业级数据库，银行电信常用
 *   5. SQL Server —— 微软出品，Windows 环境常用
 *
 * 【非关系型数据库（NoSQL）—— 了解即可】
 *
 * 不使用表格，而是用其他方式存储数据
 *   1. Redis     —— 键值对存储，速度极快（缓存）
 *   2. MongoDB   —— 文档存储，灵活（日志数据）
 *   3. Neo4j     —— 图数据库（社交网络关系）
 *
 * 本课程我们学习的是 SQL（结构化查询语言）
 * 它是操作所有关系型数据库的"通用语言"
 */

-- =============================================
-- 第三节：什么是 SQL？
-- =============================================

/*
 * 【SQL 是什么？】
 *
 * SQL = Structured Query Language = 结构化查询语言
 * 它是操作数据库的"语言"
 *
 * 生活类比：
 *   如果数据库是图书馆，那 SQL 就是你和图书管理员沟通的语言
 *   - "我想看所有的科幻小说" → SELECT 查询
 *   - "我要借这本书" → INSERT 插入
 *   - "我要修改我的借阅信息" → UPDATE 更新
 *   - "我要还这本书" → DELETE 删除
 *
 * SQL 的分类：
 *   1. DDL（数据定义语言）—— 建造图书馆
 *      CREATE（创建）、ALTER（修改）、DROP（删除）
 *   2. DML（数据操作语言）—— 在图书馆里操作
 *      INSERT（插入）、UPDATE（更新）、DELETE（删除）
 *   3. DQL（数据查询语言）—— 在图书馆里查找
 *      SELECT（查询）—— 这是最重要的！
 *   4. DCL（数据控制语言）—— 图书馆的权限管理
 *      GRANT（授权）、REVOKE（撤销权限）
 */


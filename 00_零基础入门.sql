-- =============================================
-- 第五节：SQL 的基本规则
-- =============================================

/*
 * 【SQL 语句的书写规则】
 *
 * 1. 每条 SQL 语句以分号 ; 结尾
 *    就像中文句子用句号。结尾一样
 *
 * 2. SQL 关键字不区分大小写
 *    SELECT、select、Select 都是一样的
 *    但建议关键字用大写，表名和列名用小写（规范）
 *
 * 3. 字符串用单引号 ' ' 包裹
 *    注意：不是双引号！（虽然有些数据库也支持双引号）
 *    例如：'Hello'，"Hello"（不推荐）
 *
 * 4. 注释有两种：
 *    单行注释：-- 这是注释
 *    多行注释：/* 这是多行注释 */
 *
 * 5. SQL 语句可以换行书写
 *    一条语句可以写在多行上，提高可读性
 */

-- =============================================
-- 第六节：动手实践 —— 创建第一个表
-- =============================================

/*
 * 【创建表】
 *
 * 就像图书馆新建一个书架，你需要告诉管理员：
 * - 这个书架叫什么名字
 * - 每本书要记录哪些信息
 *
 * 语法：CREATE TABLE 表名 (列名1 类型, 列名2 类型, ...);
 */

-- 创建一个"学生"表
CREATE TABLE IF NOT EXISTS students (
    id      INTEGER PRIMARY KEY,  -- 学号（主键，唯一标识）
    name    TEXT NOT NULL,         -- 姓名（不能为空）
    age     INTEGER,               -- 年龄
    grade   TEXT,                  -- 年级
    city    TEXT                   -- 城市
);

/*
 * 逐行解释：
 * CREATE TABLE students  —— 创建一个叫 students 的表
 * IF NOT EXISTS          —— 如果这个表已经存在，就不要重复创建（避免报错）
 * id INTEGER PRIMARY KEY —— id 列，整数类型，是主键（每行的唯一身份证）
 * name TEXT NOT NULL     —— name 列，文本类型，不能为空（必须填）
 * age INTEGER            —— age 列，整数类型
 * grade TEXT             —— grade 列，文本类型
 * city TEXT              —— city 列，文本类型
 */

-- 向表中插入一些示例数据
INSERT INTO students (id, name, age, grade, city) VALUES (1, '张三', 18, '大一', '北京');
INSERT INTO students (id, name, age, grade, city) VALUES (2, '李四', 19, '大二', '上海');
INSERT INTO students (id, name, age, grade, city) VALUES (3, '王五', 20, '大三', '广州');
INSERT INTO students (id, name, age, grade, city) VALUES (4, '赵六', 18, '大一', '深圳');
INSERT INTO students (id, name, age, grade, city) VALUES (5, '钱七', 21, '大四', '杭州');

/*
 * INSERT INTO students —— 向 students 表插入数据
 * (id, name, age, grade, city) —— 指定要填哪些列
 * VALUES (1, '张三', 18, '大一', '北京') —— 具体的值
 */

-- =============================================
-- 第七节：你的第一个查询
-- =============================================

-- 查询所有学生的所有信息
-- * 表示"所有列"
SELECT * FROM students;

/*
 * 执行结果应该是：
 * id | name | age | grade | city
 * ---+------+------+-------+------
 * 1  | 张三 | 18   | 大一  | 北京
 * 2  | 李四 | 19   | 大二  | 上海
 * 3  | 王五 | 20   | 大三  | 广州
 * 4  | 赵六 | 18   | 大一  | 深圳
 * 5  | 钱七 | 21   | 大四  | 杭州
 */

-- 只查询学生的姓名和年龄
SELECT name, age FROM students;

-- 给列起别名（AS 关键字）
SELECT name AS 姓名, age AS 年龄 FROM students;

-- 查询时进行简单的计算
SELECT name AS 姓名, age AS 当前年龄, age + 1 AS 明年年龄 FROM students;

-- =============================================
-- 第八节：条件查询初体验
-- =============================================

/*
 * 【WHERE 子句】
 *
 * 如果你只想看满足某些条件的数据，就需要 WHERE
 * 就像你对图书管理员说："我只想看2020年以后出版的书"
 *
 * 语法：SELECT 列名 FROM 表名 WHERE 条件;
 */

-- 查询年龄等于18岁的学生
SELECT * FROM students WHERE age = 18;

-- 查询年龄大于19岁的学生
SELECT * FROM students WHERE age > 19;

-- 查询来自北京的学生
SELECT * FROM students WHERE city = '北京';


 * 练习3：创建表
 * Q: 创建一个"课程"表，包含：课程ID（主键）、课程名、学分、教师
 * A:
 */

CREATE TABLE IF NOT EXISTS courses (
    course_id   INTEGER PRIMARY KEY,
    course_name TEXT NOT NULL,
    credit      INTEGER,
    teacher     TEXT
);

-- 插入示例数据
INSERT INTO courses VALUES (1, '高等数学', 4, '张教授');
INSERT INTO courses VALUES (2, '英语', 3, '李教授');
INSERT INTO courses VALUES (3, '计算机基础', 2, '王教授');

-- 验证
SELECT * FROM courses;

/*
 * 练习4：条件查询
 * Q: 查询 students 表中，来自"上海"的学生姓名
 * A:
 */

SELECT name FROM students WHERE city = '上海';

/*
 * 练习5：综合练习
 * Q: 创建一个"图书"表，包含以下字段：
 *    - 图书ID（主键）
 *    - 书名（不能为空）
 *    - 作者
 *    - 价格（小数）
 *    - 出版年份
 *    然后插入3条数据，并查询所有价格大于50的书
 * A:
 */

CREATE TABLE IF NOT EXISTS books (
    book_id     INTEGER PRIMARY KEY,
    title       TEXT NOT NULL,
    author      TEXT,
    price       REAL,
    pub_year    INTEGER
);

INSERT INTO books VALUES (1, '红楼梦', '曹雪芹', 68.00, 1791);
INSERT INTO books VALUES (2, '三体', '刘慈欣', 45.00, 2008);
INSERT INTO books VALUES (3, '活着', '余华', 35.00, 1993);
INSERT INTO books VALUES (4, '百年孤独', '马尔克斯', 55.00, 1967);

-- 查询价格大于50的书
SELECT * FROM books WHERE price > 50;

